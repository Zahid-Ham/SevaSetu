def test_health():
    print("=== Testing Health Check ===")
    r = requests.get(f"{API_URL}/")
    print(f"Status: {r.status_code}")
    print(f"Response: {r.json()}")
    print()

def test_scan_form():
    print("=== Testing /scan-form ===")
    import tempfile
    with tempfile.NamedTemporaryFile(delete=False, suffix='.jpg') as f:
        f.write(JPEG_BYTES)
        tmp_path = f.name

    print(f"Sending test image: {tmp_path}")
    with open(tmp_path, "rb") as img:
        r = requests.post(
            f"{API_URL}/scan-form",
            files={"file": ("test.jpg", img, "image/jpeg")}
        )
    
    print(f"Status: {r.status_code}")
    try:
        body = r.json()
        import json
        print(f"Response: {json.dumps(body, indent=2)}")
    except Exception:
        print(f"Raw response: {r.text[:3000]}")
    
    os.unlink(tmp_path)

if __name__ == "__main__":
    test_health()
    test_scan_form()
